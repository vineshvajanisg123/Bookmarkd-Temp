export function calculateFallbackProfile(
  lovedBook: string,
  genrePref: string,
  stylePref: string,
  goalPref: string,
  selfDef?: string
): ReadingProfile {
  const normBook = (lovedBook || "").toLowerCase();
  
  if (
    normBook.includes("courage") || 
    normBook.includes("disliked") || 
    normBook.includes("habit") || 
    normBook.includes("clear") || 
    normBook.includes("dog")
  ) {
    return FALLBACK_ARCHETYPES[1]; // Growth Architect
  }
  if (
    normBook.includes("think") || 
    normBook.includes("slow") || 
    normBook.includes("work") || 
    normBook.includes("focus")
  ) {
    return FALLBACK_ARCHETYPES[0]; // Evidence Seeker
  }
  if (
    normBook.includes("creative") || 
    normBook.includes("rubin") || 
    normBook.includes("meaning") || 
    normBook.includes("frankl") || 
    normBook.includes("siddhartha")
  ) {
    return FALLBACK_ARCHETYPES[2]; // Deep Thinker
  }
  if (
    normBook.includes("alchemist") || 
    normBook.includes("dune") || 
    normBook.includes("gatsby") || 
    normBook.includes("orwell") || 
    normBook.includes("1984")
  ) {
    return FALLBACK_ARCHETYPES[3]; // Story Explorer
  }

  // Fallbacks based on category selections
  if (genrePref === "Fiction") {
    return FALLBACK_ARCHETYPES[3]; // Story Explorer
  }
  if (goalPref === "Learning" && stylePref === "Fast and engaging") {
    return FALLBACK_ARCHETYPES[1]; // Growth Architect
  }
  if (goalPref === "Learning" && stylePref === "Deep and thoughtful") {
    return FALLBACK_ARCHETYPES[0]; // Evidence Seeker
  }
  if (stylePref === "Deep and thoughtful") {
    return FALLBACK_ARCHETYPES[2]; // Deep Thinker
  }

  return FALLBACK_ARCHETYPES[1]; // Default to Growth Architect
}
